# 7. REPORT
#
# Purpose:
#   Each row is one AI-generated report.  A single session can produce
#   multiple report types (career, field_scope, field_comparison) and
#   multiple runs of the same type (regeneration).
#
#   session_id is nullable because field-scope and field-comparison reports
#   do not require a completed student session — they are general queries.
#
#   The status column enables async generation:
#     pending → generating → complete | failed
#   The API returns immediately and the frontend polls GET /reports/{id}/status.
#
#   generation_duration_ms captures how long the full AI pipeline took so
#   slow sections can be identified and optimised.
# ---------------------------------------------------------------------------

class Report(Base):
    __tablename__ = "reports"

    # -- Primary key ---------------------------------------------------------
    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        primary_key=True,
        server_default=func.gen_random_uuid(),
    )

    # -- Foreign keys --------------------------------------------------------
    session_id: Mapped[Optional[uuid.UUID]] = mapped_column(
        UUID(as_uuid=True),
        ForeignKey("sessions.id", ondelete="CASCADE"),
        nullable=True,
        index=True,
        comment="NULL for field-scope / field-comparison reports (no session needed)",
    )
    user_id: Mapped[Optional[uuid.UUID]] = mapped_column(
        UUID(as_uuid=True),
        ForeignKey("users.id", ondelete="SET NULL"),
        nullable=True,
        index=True,
        comment="Denormalized for fast access-control checks without joining Session",
    )

    # -- Report identity -----------------------------------------------------
    report_type: Mapped[str] = mapped_column(
        String(30),
        nullable=False,
        index=True,
        comment="career | field_scope | field_comparison",
    )

    # -- Content -------------------------------------------------------------
    report_text: Mapped[str] = mapped_column(
        Text,
        nullable=False,
        comment="Full markdown output from the AI model",
    )
    pdf_path: Mapped[Optional[str]] = mapped_column(
        String(500),
        nullable=True,
        comment="Relative filesystem path or S3 key — never expose directly to users",
    )

    # -- Generation lifecycle ------------------------------------------------
    status: Mapped[str] = mapped_column(
        String(20),
        nullable=False,
        server_default="pending",
        index=True,
        comment="pending | generating | complete | failed",
    )
    generation_duration_ms: Mapped[Optional[int]] = mapped_column(
        Integer,
        nullable=True,
        comment="Wall-clock ms for the entire AI generation pipeline",
    )

    # -- Audit timestamps ----------------------------------------------------
    generated_at: Mapped[datetime] = mapped_column(
        nullable=False,
        server_default=func.now(),
        index=True,
    )
    expires_at: Mapped[Optional[datetime]] = mapped_column(
        nullable=True,
        index=True,
        comment="Scheduled cleanup — delete PDF and null report_text after this date",
    )

    # -- Constraints ---------------------------------------------------------
    __table_args__ = (
        CheckConstraint(
            "report_type IN ('career', 'field_scope', 'field_comparison')",
            name="ck_reports_report_type_values",
        ),
        CheckConstraint(
            "status IN ('pending', 'generating', 'complete', 'failed')",
            name="ck_reports_status_values",
        ),
        CheckConstraint(
            "generation_duration_ms >= 0",
            name="ck_reports_duration_non_negative",
        ),
    )

    # -- Relationships -------------------------------------------------------
    session: Mapped[Optional["Session"]] = relationship(
        "Session",
        back_populates="reports",
    )
    user: Mapped[Optional["User"]] = relationship(
        "User",
        back_populates="reports",
    )
    sections: Mapped[List["ReportSection"]] = relationship(
        "ReportSection",
        back_populates="report",
        order_by="ReportSection.order_index",   # always retrieved in display order
        cascade="all, delete-orphan",
        passive_deletes=True,
    )

    def __repr__(self) -> str:
        return (
            f"<Report id={self.id} "
            f"type={self.report_type!r} "
            f"status={self.status!r}>"
        )


# ---------------------------------------------------------------------------
# 8. REPORT SECTION
#
# Purpose:
#   Stores the output of each individual API call made during report
#   generation.  The existing report_generator.py makes multiple sequential
#   calls — one per section.  This table enables:
#
#     • Section-level resumability: if call 7 of 12 fails, only that section
#       is regenerated on retry.
#     • Observability: token_count per section surfaces which sections are
#       expensive.
#     • Future caching: if a field-scope section for "Medicine" already exists
#       and is recent enough, skip regenerating it.
#
# Unique constraint:
#   (report_id, section_key) — one text blob per named section per report.
#
# order_index:
#   The Report relationship orders by this column so sections are always
#   returned in the correct display sequence without ORDER BY in queries.
# ---------------------------------------------------------------------------

class ReportSection(Base):
    __tablename__ = "report_sections"

    # -- Primary key ---------------------------------------------------------
    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        primary_key=True,
        server_default=func.gen_random_uuid(),
    )

    # -- Foreign key ---------------------------------------------------------
    report_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        ForeignKey("reports.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )

    # -- Data ----------------------------------------------------------------
    section_key: Mapped[str] = mapped_column(
        String(100),
        nullable=False,
        comment="Machine-readable section identifier e.g. 'career_matches', 'skill_gap'",
    )
    content: Mapped[str] = mapped_column(
        Text,
        nullable=False,
        comment="Raw text output returned by the AI for this section",
    )
    token_count: Mapped[Optional[int]] = mapped_column(
        Integer,
        nullable=True,
        comment="Estimated via estimate_tokens() in api_client.py",
    )
    order_index: Mapped[int] = mapped_column(
        SmallInteger,
        nullable=False,
        comment="Display ordering — Report.sections relationship sorts by this",
    )

    # -- Audit ---------------------------------------------------------------
    generated_at: Mapped[datetime] = mapped_column(
        nullable=False,
        server_default=func.now(),
    )

    # -- Constraints ---------------------------------------------------------
    __table_args__ = (
        UniqueConstraint(
            "report_id", "section_key",
            name="uq_report_sections_report_key",
        ),
        CheckConstraint(
            "token_count >= 0",
            name="ck_report_sections_token_count_non_negative",
        ),
        CheckConstraint(
            "order_index >= 0",
            name="ck_report_sections_order_index_non_negative",
        ),
    )

    # -- Relationship --------------------------------------------------------
    report: Mapped["Report"] = relationship(
        "Report",
        back_populates="sections",
    )

    def __repr__(self) -> str:
        return (
            f"<ReportSection key={self.section_key!r} "
            f"order={self.order_index} "
            f"tokens={self.token_count}>"
        )